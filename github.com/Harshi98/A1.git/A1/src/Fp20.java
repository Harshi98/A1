import java.util.*;
public class Fp20 {
	public static void main(String []args)
	{
		 int n = 100; 
		  for (int num = 0; num < n; num++) 
	        {        
	            if (num % 3 == 0 && num % 5 == 0) 
	                System.out.print(num + " "); 
	        } 
		  System.out.println(""+n);
	}

}
